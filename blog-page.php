      <h2 class="blog-page__title">3 совета по уходу за кузовом</h2>
      <p class="advice__title">1. Двухфазная мойка</p>
      <p>Выше мы уже говорили о том, что не рекомендуется мыть кузов сразу после проведения процедур. Советуем
        воздержаться от мойки в первые 3-4 недели. В последующем стоит помнить, что контактная мойка может повредить
        покрытие, поэтому нужно соблюдать главное правило — прибегать к профессиональной двухфазной мойке. Что она в
        себя включает?
        <br>
        <br>
        Первая фаза — бесконтактная мойка. На данном этапе наносится шампунь с щёлочью в той пропорции, которая не
        вредит кузову. Затем автомобиль вымывают мягкой губкой со специальным раствором для ручной мойки. Начинать
        обязательно всегда с крыши и вниз. После протирки каждой детали губка выжимается для удаления грязной пены.</p>
      <p class="advice__title">2. Исключение абразивного воздействия</p>
      <p>Что это значит? Оградите свой автомобиль от любого внешнего механического воздействия после обработки. Дело в
        том, что она отлично защищает от вредных реагентов, песка и солнца, но не гарантирует спасение от серьёзных
        повреждений, в том числе царапин на парковке или от бордюров. Во избежание микротрещин также не используйте для
        мойки щётку и тряпки. Не трите сильно поверхность и возьмите мягкие губки.
        <br>
        <br>
        Будьте осторожны в эксплуатации вашего авто. Если вы всё будете делать правильно и избегать сильных воздействий,
        то защита продержится долгие годы, радуя вас сочным цветом. Но даже в случае появления сколов или царапин
        расстраиваться не стоит. Обращайтесь за ТО к мастерам, и они обязательно исправят недостатки в короткие сроки.
      </p>
